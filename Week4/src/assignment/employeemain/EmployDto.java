package assignment.employeemain;

public class EmployDto {
	private int employId;
	private String employName;
	private String employAddress;
	private float employSalary;
	private int employContact;
	private int departId;
	private String departName;
	private String departHead;
	private String departDescription;
	
	public int getEmployId() {
		return employId;
	}
	public void setEmployId(int employId) {
		this.employId = employId;
	}
	public String getEmployName() {
		return employName;
	}
	public void setEmployName(String employName) {
		this.employName = employName;
	}
	public String getEmployAddress() {
		return employAddress;
	}
	public void setEmployAddress(String employAddress) {
		this.employAddress = employAddress;
	}
	public float getEmploySalary() {
		return employSalary;
	}
	public void setEmploySalary(float employSalary) {
		this.employSalary = employSalary;
	}
	public int getEmployContact() {
		return employContact;
	}
	public void setEmployContact(int employContact) {
		this.employContact = employContact;
	}
	public int getDepartId() {
		return departId;
	}
	public void setDepartId(int departId) {
		this.departId = departId;
	}
	public String getDepartName() {
		return departName;
	}
	public void setDepartName(String departName) {
		this.departName = departName;
	}
	public String getDepartHead() {
		return departHead;
	}
	public void setDepartHead(String departHead) {
		this.departHead = departHead;
	}
	public String getDepartDescription() {
		return departDescription;
	}
	public void setDepartDescription(String departDescription) {
		this.departDescription = departDescription;
	}
	

}
